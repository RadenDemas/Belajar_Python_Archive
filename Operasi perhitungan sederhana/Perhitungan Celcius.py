#Perhitungan konversi suatu temperatur ke temperatur yang lain
#Perhitungan konversi celcius ke temperatur yang lian

Celcius = float(input("Berapa derajat celcius?"))
print("Celcius", Celcius,"째")

#reamur
#konversi celcius ke reamur = 4/5
reamur = Celcius * 4 / 5
print("Reamur", reamur, "째")

#farenheit
#konversi celcius ke farenheit = (celcius * 9 / 5) + 32
farenheit = (Celcius * 9 / 5) + 32
print("farenheit", farenheit, "째")

#kelvin
#konversi celcius ke kelvin + 273
kelvin = Celcius + 273
print("Kelvin", kelvin, "째")