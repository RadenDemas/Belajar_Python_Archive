a = 7
b = 3

#operasi tambah (+)
hasil = a + b
print(a, '+', b, '=', hasil)
print("Data tipe", type(hasil))

#operasi pengurangan (-)
hasil = a - b
print(a, '-', b, '=', hasil)

#operasi perkaulian (*)
hasil = a * b
print(a, '*', b, '=', hasil)

#operasi pembagian (/)
hasil = a / b
print(a, '/', b, '=', hasil)
print("Data tipe: ", type(hasil))

#operasi eksponen atau perpangkatan (**)
hasil = a ** b
print(a, '**', b, '=', hasil)

#operasi modulus atau hasil sisa pembagian (%)
#Contoh 7 : 3 = 6 sisa 1
#jadi modulus nya adalah 1
hasil = a % b
print(a, '%', b, '=', hasil)

#operasi floor divison (//)
#floor divison merupakan pembulatan kebawah misal 17.8 akan menjadi 17
hasil = a // b
print(a, '//', b, '=', hasil)

#seluruh hasil jika tidak ada desimal akan berupa integer
#seluruh hasil jika ada desimal akan berupa float